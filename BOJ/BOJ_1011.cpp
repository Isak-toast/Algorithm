#include <stdio.h>
/*
현재 위치에서 목표 위치로 이동하는데 걸리는 최소 이동 횟수를 구하는 문제

이 코드에서 사용된 반복 패턴은 일반적으로 "누적 합" 또는 "부분 합"이라고 부릅니다. 
이 패턴은 주어진 수열의 연속된 구간의 합을 구하는 과정에서 많이 사용되며, 누적 합을 구하여 목표 값을 얻을 때까지 반복하는 방식입니다.

위 코드에서는 각 이동 횟수에 따라 이동 거리를 누적하며 목표 거리에 도달할 때까지 반복하는 형태를 보입니다. 
이러한 누적 합 패턴은 다양한 문제 상황에서 활용되며, 효율적인 알고리즘 구현에 도움이 됩니다.
*/

main()
{
    size_t t, x, y, d, j;   // t: 테스트 케이스, x: 현재 위치, y: 목표 위치, d: 이동 거리, j: 이동 횟수
    scanf("%d", &t);        // 테스트 케이스 입력
    for (size_t i = 0; i < t; ++i) //   테스트 케이스 수 만큼 반복
    {
        scanf("%d%d", &x, &y);  // 현재 위치, 목표 위치 입력
        j = d = j ^ j;      // 이동 횟수, 이동 거리 초기화
        while (d < y - x)   // 이동 거리가 목표 거리보다 작을 때까지 반복
            d += ++j / 2;   // 이동 거리에 이동 횟수의 절반을 더함
        printf("%d\n", j - 1); // 이동 횟수 출력
    }
}